void main(List<String> args) {
  // tipedata dynamic
  /** 
    * memungkinkan menampung type data secara realtif secara beragntian
    */

  dynamic varibel = 100;
  print(varibel);

  varibel = true;
  print(varibel);

  varibel = 200;
  print(varibel);

  varibel = 20.0;
  print(varibel);

  varibel = 'khab';
  print(varibel);
}
